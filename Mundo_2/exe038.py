num1 = int (input ('Primeiro número: '))
num2 = int (input ('Segundo número: '))
if num1 > num2:
    print ('O primeiro número é maior que o segundo.')
elif num2 > num1:
    print ('O Segundo número é maior que o primeiro.')
elif num1 == num2:
    print ('Não existe valor maior, os dois são iguais.')
